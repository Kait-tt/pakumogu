function collisionToBackGround(collisionData){
	var backgrondData = [[]];
	for(var i=0;i<collisionData.length;i++){
		for(var j=0;j<collisionData[i].length;j++){
			//0 = walkable
			if(collisionData[i][j] == 0){
				backgrondData[i][j] = 2; //sand
			}else {
				backgrondData[i][j] = 0;//grass
			}
		}
	}
}